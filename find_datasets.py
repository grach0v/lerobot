api = HfApi()
cutoff = datetime.now(timezone.utc) - timedelta(hours=24)
rows = []
json_data = []

for author in tqdm(handles, desc="Checking users"):
    try:
        # sort='lastModified' returns newest first, so we can bail early
        for ds in api.list_datasets(author=author, sort="lastModified"):
            if ds.last_modified and ds.last_modified > cutoff:
                formatted_date = ds.last_modified.strftime("%Y-%m-%d %H:%M UTC")
                rows.append([author, ds.id, formatted_date])
                json_data.append({
                    "user": author,
                    "dataset_id": ds.id,
                    "last_modified": formatted_date,
                    "last_modified_iso": ds.last_modified.isoformat()
                })
            else:
                break  # older than 24 h → stop checking this user
    except Exception as e:
        print(f"⚠️  {author}: {e}")

if rows:
    print(tabulate(rows, headers=["User", "Dataset", "Last Modified"]))
    
    # Save to JSON file
    output_file = f"datasets_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
    with open(output_file, 'w') as f:
        json.dump({
            "generated_at": datetime.now().isoformat(),
            "cutoff_time": cutoff.isoformat(),
            "total_datasets": len(json_data),
            "datasets": json_data
        }, f, indent=2)
    print(f"✅ Results saved to {output_file}")
else:
    print("No new datasets in the past 24 h from the provided handles.")
